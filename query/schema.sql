-- ============================================================
-- sentences
-- ============================================================
CREATE TABLE public.sentences (
    id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    sentence text,
    meaning text,
    reading text,
    created_at timestamp with time zone NOT NULL DEFAULT now(),
    updated_at timestamp with time zone DEFAULT now(),
    word_id bigint REFERENCES public.kotoba(id),
    context_id bigint REFERENCES public.context(id)
);

COMMENT ON TABLE public.sentences IS 'Phrases and sentences captured from photos, like 熊に注意 or ここで喫煙しないでください.';

CREATE INDEX idx_sentences_word_id ON public.sentences (word_id);
CREATE INDEX idx_sentences_context_id ON public.sentences (context_id);

ALTER TABLE public.sentences ENABLE ROW LEVEL SECURITY;

CREATE POLICY "allow all - anon" ON public.sentences FOR ALL TO anon, authenticated USING (true) WITH CHECK (true);

-- ============================================================
-- sentence_kotoba (many-to-many: sentences <-> kotoba)
-- ============================================================
CREATE TABLE public.sentence_kotoba (
    id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    sentence_id bigint NOT NULL REFERENCES public.sentences(id),
    kotoba_id bigint NOT NULL REFERENCES public.kotoba(id),
    created_at timestamp with time zone NOT NULL DEFAULT now(),
    UNIQUE (sentence_id, kotoba_id)
);

COMMENT ON TABLE public.sentence_kotoba IS 'Every kotoba word actually used in a given sentence (not just the single word the sentence was originally written to demonstrate, via sentences.word_id).';

CREATE INDEX idx_sentence_kotoba_sentence_id ON public.sentence_kotoba (sentence_id);
CREATE INDEX idx_sentence_kotoba_kotoba_id ON public.sentence_kotoba (kotoba_id);

ALTER TABLE public.sentence_kotoba ENABLE ROW LEVEL SECURITY;

CREATE POLICY "allow all - anon" ON public.sentence_kotoba FOR ALL TO anon, authenticated USING (true) WITH CHECK (true);

-- ============================================================
-- Notes
-- ============================================================
-- Single-user app: RLS is enabled on every table with one open policy per
-- table ("allow all - anon") granting full SELECT/INSERT/UPDATE/DELETE to
-- the anon and authenticated roles. There is no user_id column anywhere and
-- no per-row ownership — access control is just "has the Supabase API key",
-- not per-user. If this ever becomes multi-user, these policies and the
-- schema (user_id columns) both need revisiting.
--
-- context.kind distinguishes "concrete" contexts (a real place you visited,
-- e.g. Restaurant, Station — originally curated as ~10-word situational
-- sets) from "abstract" contexts (a topic/semantic category, e.g. Color,
-- Time / Date — used for otherwise-unclassifiable systematic vocabulary).
--
-- source is a further refinement of context: context is the general
-- category ("Clothing Store"), source is the specific real-world instance
-- ("Uniqlo", uniqlo.com). kotoba.source_id is one-to-one per word (a word
-- has at most one recorded source), and source.context_id optionally rolls
-- a specific source up into its general category.
--
-- kotoba.kana_type classifies the word's writing system: 'kanji' if it
-- contains any kanji character (regardless of mixed-in okurigana), else
-- 'katakana' or 'hiragana' for kana-only words. Replaces the old has_kanji
-- boolean. kotoba.reading is NULL for non-kanji words (kana_type != 'kanji'),
-- since the reading would just duplicate word there.
--
-- sentences.context_id is independent of kotoba.context_id: a single word
-- can have multiple example sentences that each show it used in a different
-- real-world situation (e.g. 予約 has one sentence set in a Restaurant and
-- another set in Travel), so context is assigned per-sentence, not inherited
-- from the word. Nullable, same as kotoba.context_id.
--
-- sentence_kotoba is a further refinement of sentences.word_id: word_id is
-- the single word a sentence was originally written to demonstrate, but a
-- sentence often incidentally uses several other vocabulary words too (e.g.
-- "会社の利益が増加した。" uses 会社, 利益, and 増加). sentence_kotoba records
-- every kotoba word genuinely present in a sentence (including its own
-- word_id), letting the UI show a sentence's full vocabulary breakdown by
-- part of speech instead of just its one primary word.
--
-- kanji.cluster is a free-text thematic grouping independent of grade/JLPT,
-- e.g. 一/二/三 belong to cluster 'number'. Nullable, plain text (no fixed
-- enum), same convention as kotoba.jlpt/grade.
